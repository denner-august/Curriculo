document.getElementById('mobile-open').onclick = ()=>{
    document.getElementById('menu-header').classList.remove('open')
}

document.getElementById('close').onclick = ()=>{
    document.getElementById('menu-header').classList.add('open')
}

document.getElementById('projeto-open').onclick = ()=>{
    document.getElementById('projetos').classList.remove('projetos-open')
}

document.getElementById('projetos-close').onclick = ()=>{
   document.getElementById("projetos").classList.add('projetos-open')
}
  